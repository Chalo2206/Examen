    private void cargarAutor() {
        List<Autor> autor = autorDao.listar();
        DefaultTableModel modelo=(DefaultTableModel)tblPuestos.getModel();
        modelo.getDataVector().clear();
        for(int i =0;i<autor.size();i++){
            modelo.addRow(new Object[]{autor.get(i).getNombres(), autor.get(i).getApellidos()});            
        }
    }
