    private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed
        
        if(operacion  == CREAR){
            List<Autor> conta = autorDao.listar();            
            Autor autor = new Autor(conta.size()+1, txtNombres.getText(), txtApellidos.getText());
            AutorDao autorDao = new AutorImpl();
            autorDao.guardar(autor);
            cargarAutor();
        }
    }//GEN-LAST:event_btnNuevoActionPerformed

    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        int resultado = JOptionPane.showConfirmDialog(this,"Â¿desea borrar al Autor", "Confirmacion",JOptionPane.YES_NO_CANCEL_OPTION);
        if(resultado == JOptionPane.YES_OPTION){
            int posicion = tblPuestos.getSelectedRow();
            DefaultTableModel modelo = (DefaultTableModel)tblPuestos.getModel();
            Autor autor = new Autor((int) modelo.getValueAt(posicion, 0));
            autorDao.borrar(autor);            
            cargarAutor();
        }
    }//GEN-LAST:event_btnBorrarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        if(operacion < MODIFICAR){
            int posicion = tblPuestos.getSelectedRow();
            Autor autor = new Autor(posicion, txtNombres.getText(), txtApellidos.getText());
            AutorDao autorDao = new AutorImpl();
            autorDao.modificar(autor);
            cargarAutor();
        }
    }//GEN-LAST:event_btnModificarActionPerformed

  
