    private void cargarLibro() {
        List<Libro> libro = libroDao.listar();
        List<Autor> autor = autorDao.listar();
        DefaultTableModel modelo=(DefaultTableModel)tblLibros.getModel();
        modelo.getDataVector().clear();
        for(int i =0;i<libro.size();i++){
            modelo.addRow(new Object[]{libro.get(i).getNombre(),libro.get(i).getCategoria(), libro.get(i).getIsbn(), autor.get(i).getNombres()});            
        }
    }  
